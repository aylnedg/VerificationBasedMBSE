%GENERATEMCREPORT Generate Monte Carlo HTML report.
projRoot = 'C:/UAV/VerificationBasedMBSE';
matFile  = fullfile(projRoot, 'Verification/reports/montecarlo_results.mat');
load(matFile, 'mc_results');

N         = mc_results.N;
n_ok      = N - mc_results.n_crashed;
n_fault   = mc_results.n_faults;
fault_rate = 100 * n_fault / n_ok;
ff_pct    = mc_results.ff_mean * 100;
dt        = datestr(now, 'yyyy-mm-dd HH:MM');
mass_arr  = mc_results.UAVmass;
land_arr  = mc_results.landTime;
ff        = mc_results.fault_fraction;

r_vals      = [mc_results.r_mass_ff; mc_results.r_land_ff; mc_results.r_wpx_ff; mc_results.r_wpy_ff; mc_results.r_wpz_ff];
param_names = {'UAVmass (kg)', 'landTime (s)', 'Waypoint dX (m)', 'Waypoint dY (m)', 'Waypoint dZ (m)'};
sens_labels = cell(5,1);
for k = 1:5
    ar = abs(r_vals(k));
    if ar < 0.1;      sens_labels{k} = 'Negligible';
    elseif ar < 0.3;  sens_labels{k} = 'Weak';
    elseif ar < 0.5;  sens_labels{k} = 'Moderate';
    else;             sens_labels{k} = 'Strong';
    end
end

css = ['<style>'...
'body{font-family:Arial,sans-serif;background:#f4f7fc;color:#222;margin:0;padding:0}'...
